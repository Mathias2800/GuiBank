    private void vaildate() {
        if(!jTextField1.getText().isEmpty() && !jTextField2.getText().isEmpty() && !jTextField3.getText().isEmpty() ){
           jLabel4.setText("You have submit your loan request");
       }else{
          if(jTextField1.getText().isEmpty()){
              jLabel4.setText("Missing a ssn");
          }else if (jTextField2.getText().isEmpty()){
              jLabel4.setText("Missing the amount of the loan ");
          }else if (jTextField3.getText().isEmpty()){
              jLabel4.setText("Missing the Duration of the loan ");
          }
       }  
       
        if(!jTextField1.getText().isEmpty()){
             
             String ssn1 = jTextField1.getText();
             String ssn2 = jTextField1.getText(); 
             try{
                 Integer.parseInt(ssn1.substring(0, 5));
             }catch (Exception ex) {
                 jLabel4.setText("<html>The SSN dosn't match <br>the wanted format of xxxxxx-xxxx</html>");
             }
             char temp = ssn1.charAt(6);
             if(temp != '-' ){
                jLabel4.setText("<html>The SSN dosn't match <br>the wanted format of xxxxxx-xxxx</html>");
             }
             try{
                 Integer.parseInt(ssn2.substring(8, 11));
             }catch (Exception ex) {
                 jLabel4.setText("<html>The SSN dosn't match <br>the wanted format of xxxxxx-xxxx</html>");
             }
        }
        
        else if(!jTextField2.getText().isEmpty()){
             try{
                Double.parseDouble(jTextField2.getText());
             }catch (NumberFormatException ex) {
                 jLabel4.setText("Please type in a number");
             }
        }
        else if(!jTextField3.getText().isEmpty()){
             try{
                Integer.parseInt(jTextField2.getText());
             }catch (NumberFormatException ex) {
                 jLabel4.setText("Please type in a number");
             }
        }
    
    }
